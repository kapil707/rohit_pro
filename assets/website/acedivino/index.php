        function submit_enquiry_form() {
            name = $('#name').val();
            emailid = $('#email').val();
            contact = $('#contact').val();
            msg = $('#msg').val();

            if ($.trim(name) == "") {
                $('#name').css('border', '1px solid #ff363a');
                $('#name').focus();
                return false;
            } else if ($.trim(emailid) == "") {
                $('#email').css('border', '1px solid #ff363a');
                $('#email').focus();
                return false;
            } else if (!validateEmail(emailid)) {
                $('#email').css('border', '1px solid #ff363a');
                $('#email').focus();
                return false;
            } else if ($.trim(contact) == "") {
                $('#contact').css('border', '1px solid #ff363a');
                $('#contact').focus();
                return false;
            } else if (contact.length < 6) {
                $('#contact').css('border', '1px solid #ff363a');
                $('#contact').focus();
                return false;
            } else if (!contact.match(/[0-9]+/)) {
                $('#contact').css('border-color', 'red');
                $('#contact').focus();
                return false;
            } else {

                $("#loaderIcon").show();
                jQuery.ajax({

                    url: "contact_process.php/",
                    data: 'name=' + name + '&email=' + emailid + '&contact=' + contact + '&msg=' + msg,
                    type: "POST",

                    success: function(data) {
                        //alert(data); return false; 
                        $("#loaderIcon").hide();
                        $("#screen-reader-response").html(data);
                        //$('#newsletter').val("");

                    },
                    error: function() {}
                })


            }
        }

        function validateEmail($email) {
            var emailReg = /^([\w-\.]+@([\w-]+\.)+[\w-]{2,4})?$/;
            return emailReg.test($email);
        }
    </script>
